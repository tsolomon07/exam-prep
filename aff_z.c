#include	<unistd.h>

int		main(int argc, char **argv)
{
	int t;

	t = 0;
	if (argc != 2)
	{
		write(1,"z\n", 2);
	}
	else
	{
		while (argv[1][t] != '\0')
		{
			if (argv[1][t] == 'z')
			{
				write(1, &argv[1][t], 1);
				write(1, "\n", 1);
				return (0);
			}
			t++;
		}
	}	
	return (0);
}
